/**
 * 获取指定语言的配置
 * @param {string} lang - 语言代码 (zh, en, fr, ru, es, ar)
 * @returns {object} 语言配置对象
 */
function getLang(lang) {
  return i18n[lang] || i18n.zh; // 默认返回中文
}

/**
 * 获取支持的语言列表
 * @returns {string[]} 支持的语言代码数组
 */
function getSupportedLanguages() {
  return Object.keys(i18n);
}

